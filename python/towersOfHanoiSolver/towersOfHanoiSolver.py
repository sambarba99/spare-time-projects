# --------------------------------------------  FUNCTIONS  ------------------------------------------- #
# ---------------------------------------------------------------------------------------------------- #

def solve(n, steps, t1=1, t2=2, t3=3):
	if n:
		solve(n - 1, steps, t1, t3, t2)
		steps.append(f"Step {len(steps) + 1}: Move disc from {t1} to {t3}")
		solve(n - 1, steps, t2, t1, t3)

# ---------------------------------------------------------------------------------------------------- #
# ----------------------------------------------  MAIN  ---------------------------------------------- #
# ---------------------------------------------------------------------------------------------------- #

n = 3

steps = []
solve(n, steps)

print(*steps, sep="\n")
